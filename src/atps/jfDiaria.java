    private void jbtnCalcSubTtlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCalcSubTtlActionPerformed
        // TODO add your handling code here:

        String opcQtdC = "00.00";
        String opcCha = "00.00";
        String opc;

        switch (jcbxNumCamas.getSelectedIndex()) {

            case 0:
                opcQtdC = "00.00";
                break;
            case 1:
                opcQtdC = "00.10";
                break;
            case 2:
                opcQtdC = "00.20";
                break;
            case 3:
                opcQtdC = "00.30";
                break;
            case 4:
                opcQtdC = "00.40";
                break;
            case 5:
                opcQtdC = "00.50";
                break;

        }

        switch (jtfTipoQuarto.getText()) {

            case "Chalé":
                opcCha = "00.20";
                break;
            case "Quarto":
                opcCha = "00.00";
                break;

        }

        jtfVlrSubTtl.setText(String.valueOf(aluguel.calc_sub_ttl(jtfValorQuarto.getText().replaceAll("R", "").replaceAll(" ", "").replace("$", ""), opcQtdC, opcCha)));
    }//GEN-LAST:event_jbtnCalcSubTtlActionPerformed

