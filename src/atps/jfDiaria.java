    private void jbtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCalcularActionPerformed
        // TODO add your handling code here:
        BigDecimal aux0 = new BigDecimal("00.00");
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        // CALCULO DOS DIAS
        try {
            aluguel.calc_diaria_dia(sdf.parse(jtfDatEntrada.getText()),
                    sdf.parse(jtfDatSaida.getText()));
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Favor revisar as datas");
        }
        // CALCULO DAS HORAS
        aluguel.calc_diaria_hora((jtfHorEntrada.getText()
                + jtfMinEntrada.getText()), (jtfHorSaida.getText() + jtfMinSaida.getText()));
        //CALCULO DO VALOR
        aux0 = (new BigDecimal(aluguel.getDiarias1())).multiply(new BigDecimal(jtfVlrSubTtl.getText().replaceAll("R", "").replaceAll(" ", "").replace("$", "")));
        //EXIBIÇÃO DO VALOR TOTAL
        jlVlrTtlMostra.setText(String.valueOf(aux0));
        //EXIBIÇÃO DO RECIBO
        jtxaNota.setText("========================================"
                + "\nLocatário: " + modTabela().getValueAt(selLinha(), 1)
                + "\nData e horário de entrada: " + jtfDatEntrada.getText() + " | "
                + (jtfHorEntrada.getText() + ":" + jtfMinEntrada.getText())
                + "\nData e horário de saída: " + jtfDatSaida.getText() + " | "
                + (jtfHorSaida.getText() + ":" + jtfMinSaida.getText())
                + "\nNumero de diarias: " + aluguel.getDiarias1()
                + "\nTotal devido: R$" + aux0
                + "\n========================================");
    }//GEN-LAST:event_jbtnCalcularActionPerformed

